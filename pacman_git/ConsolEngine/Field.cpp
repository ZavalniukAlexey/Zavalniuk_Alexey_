int Field::countStars()
{
	int counter = 0;
	for (int i = 0; i < 31; i++)
		for (int j = 0; j < 29; j++)
		{
			if (this->field[i][j] == '.')
			counter++;
		}
	return counter;
}

int Field::getFieldScore()
{
	Field temp;
	int stars = temp.countStars();
	int fieldScore = (stars - (this->countStars())) * 10;
	return fieldScore;
}

